class UnaryAnd{
    
    public static void main(String a[])
    {
        //declaration of variables

        int w=11;
        int x=-12;
        boolean y=true;
        boolean z=false;
        System.out.println(~w); //this will add 1 to value of w and inverse the answer

        System.out.println(~x);  //this will add 1 to the value of x and inverse the answer
        System.out.println(!y);  //this will inverse " y "
        System.out.println(!z);  //this will inverse " z "

    }
}
