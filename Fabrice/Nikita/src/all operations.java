class Alice {
    public static void main (String a[])
    {
        
        // declaration of variables
        
        int h=10;

        int b=5;

        System.out.println(h+b);  // h + b = 10 + 5

        System.out.println(h-b); // h - b = 10 - 5

        System.out.println(h*b); // h * b = 10 * 5

        System.out.println(h/b); // h / b = 10 / 5

        System.out.println(h%b); // h % b = 10 % 5
    }
}
