 class Unary1
  {
    
    public static void main(String a[])
    {
        // declaration of variable
        
        int m=11;

        System.out.println(m++); //m is incrementing after

        System.out.println(++m); //m is incrementing before

        System.out.println(m--); //m is decrementing after

        System.out.println(--m); //m is decrementing before

    }
}
